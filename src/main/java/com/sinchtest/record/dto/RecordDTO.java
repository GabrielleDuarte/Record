package com.sinchtest.record.dto;


public class RecordDTO {
	
	private String fileName;
	
	private String executionPerImage;
	
	private String wholeExecution;
	
	private String personName;
	
	private String prediction;
	
	private String age;
	
	private String sex;
	
	private String expression;

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getExecutionPerImage() {
		return executionPerImage;
	}

	public void setExecutionPerImage(String executionPerImage) {
		this.executionPerImage = executionPerImage;
	}

	public String getWholeExecution() {
		return wholeExecution;
	}

	public void setWholeExecution(String wholeExecution) {
		this.wholeExecution = wholeExecution;
	}

	public String getPersonName() {
		return personName;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}

	public String getPrediction() {
		return prediction;
	}

	public void setPrediction(String prediction) {
		this.prediction = prediction;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getExpression() {
		return expression;
	}

	public void setExpression(String expression) {
		this.expression = expression;
	}
	
	

}
